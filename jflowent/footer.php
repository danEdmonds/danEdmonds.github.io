    (function() {
  
        var slidesContainer = document.querySelector(".carousel__slides");
        var slides = document.querySelectorAll(".carousel__slide");
        var controlPrevious = document.querySelector(".carousel__control--previous");
        var controlNext = document.querySelector(".carousel__control--next");

        var currentSlide = 0;

        controlPrevious.addEventListener("click", function() {
            
            currentSlide = currentSlide - 1;
            changeSlide(currentSlide);

            controlNext.removeAttribute("disabled");

            if (currentSlide <= 0) {
                this.setAttribute("disabled", "");
            }
            
        });

        controlNext.addEventListener("click", function() {
            
            //currentSlide = (currentSlide + 1) % slides.length;
            currentSlide = currentSlide + 1;
            changeSlide(currentSlide);

            controlPrevious.removeAttribute("disabled");

            if (currentSlide >= slides.length - 1) {
                this.setAttribute("disabled", "");
            }
            
        });

        function changeSlide(currentSlide) {
            
            for (var i = 0; i < slides.length; i++) {
                slides[i].dataset.current = "false";
            }
            slides[currentSlide].dataset.current = "true";

            var slideOffset = slides[currentSlide].offsetLeft;

            slidesContainer.style.transform = "translateX(-" + slideOffset + "px)";
            
        }
        
    })();
    
